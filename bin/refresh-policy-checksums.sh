#!/usr/bin/env bash
# bin/refresh-policy-checksums.sh
# Gate #4 v1.2 — updates .harness-state/policy-checksums.yaml with current
# SHA-256 of every file under opa/policies/ and opa/tests/. Call after any
# intentional policy change. Commit the updated YAML alongside the policy
# change with the SSH-Baseline trailer.

set -euo pipefail
REPO="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
OUT="${REPO}/opa/checksums.yaml"

{
  echo "# Auto-generated by bin/refresh-policy-checksums.sh at $(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo "# Gate #4 v1.2 — VPS pre-receive validates pushed policy/test files against these SHAs."
  echo ""
  echo "policies:"
  while IFS= read -r -d '' f; do
    rel="${f#$REPO/}"
    sha="$(shasum -a 256 "$f" 2>/dev/null | awk '{print $1}' || sha256sum "$f" | awk '{print $1}')"
    printf '  "%s": "%s"\n' "$rel" "$sha"
  done < <(find "$REPO/opa/policies" "$REPO/opa/tests" -type f -print0 2>/dev/null | sort -z)
} > "$OUT"

echo "refresh-policy-checksums: wrote $OUT"
